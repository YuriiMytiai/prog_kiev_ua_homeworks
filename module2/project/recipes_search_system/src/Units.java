public enum Units {
    Pieces {
        @Override
        public String toString() {
            return "p";
        }
    },
    Kilos {
        @Override
        public String toString() {
            return "kg";
        }
    },
    Gramms {
        @Override
        public String toString() {
            return "g";
        }
    },
    Liters {
        @Override
        public String toString() {
            return "l";
        }
    },

}
